package matcher;

public class MatcherResult {

	int left, top, right, bottom;
	double scale, score;

	public int getLeft() {
		return left;
	}

	public int getTop() {
		return top;
	}

	public int getRight() {
		return right;
	}

	public int getBottom() {
		return bottom;
	}

	public double getScale() {
		return scale;
	}

	public double getScore() {
		return score;
	}

}
